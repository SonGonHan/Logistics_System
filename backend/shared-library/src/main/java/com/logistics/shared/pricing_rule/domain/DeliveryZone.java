package com.logistics.shared.pricing_rule.domain;

/**
 * Зона доставки для правил ценообразования.
 *
 * <h2>Назначение</h2>
 * Определяет географическую зону доставки груза:
 * - CITY - доставка в пределах города
 * - INTERCITY - межгородская доставка
 * - INTERNATIONAL - международная доставка
 *
 * <h2>Использование</h2>
 * Применяется для фильтрации правил ценообразования по зоне доставки.
 * Каждому правилу соответствует ровно одна зона.
 */
public enum DeliveryZone {
    CITY,
    INTERCITY,
    INTERNATIONAL
}
